/**
 * @desc    Create new order from cart
 * @route   POST /api/orders
 * @access  Private
 */
const createOrder = async (req, res, next) => {
    // Use session for transaction
    const session = await mongoose.startSession();
    session.startTransaction();
    
    try {
        const { paymentMethod, deliveryAddress, specialInstructions } = req.body;
        const userId = req.user._id;

        // Validate required fields
        if (!paymentMethod) {
            return res.status(400).json({
                success: false,
                message: 'Payment method is required'
            });
        }

        if (!deliveryAddress?.street || !deliveryAddress?.city) {
            return res.status(400).json({
                success: false,
                message: 'Complete delivery address is required'
            });
        }

        // Get user's cart
        const cart = await Cart.findOne({ user: userId })
            .populate('items.foodId', 'name price image isAvailable')
            .session(session);

        if (!cart || cart.items.length === 0) {
            return res.status(400).json({
                success: false,
                message: 'Cart is empty'
            });
        }

        // Validate all items are still available
        const orderItems = [];
        let subtotal = 0;

        for (const item of cart.items) {
            const food = item.foodId;
            
            if (!food || !food.isAvailable) {
                return res.status(400).json({
                    success: false,
                    message: `${item.name} is no longer available`
                });
            }

            const totalPrice = food.price * item.quantity;
            
            orderItems.push({
                foodId: food._id,
                name: food.name,
                quantity: item.quantity,
                price: food.price,
                totalPrice,
                snapshot: {
                    name: food.name,
                    price: food.price,
                    image: food.image,
                    isVegetarian: food.isVegetarian
                }
            });

            subtotal += totalPrice;
        }

        // Calculate tax and delivery
        const { tax, deliveryFee, total } = calculateOrderTotals(subtotal);

        // Get user details
        const user = await User.findById(userId).session(session);

        // Create order (orderNumber auto-generated by model pre-save)
        const order = await Order.create([{
            user: userId,
            userDetails: {
                name: user.name,
                email: user.email,
                phone: user.phone
            },
            items: orderItems,
            deliveryAddress,
            subtotal,
            tax,
            deliveryFee,
            totalAmount: total,
            paymentMethod,
            paymentStatus: paymentMethod === 'cash_on_delivery' ? 'pending' : 'pending',
            specialInstructions,
            statusHistory: [{
                status: 'pending',
                note: 'Order placed successfully',
                updatedBy: userId
            }]
        }], { session });

        // Clear the cart
        cart.items = [];
        cart.subtotal = 0;
        cart.totalItems = 0;
        await cart.save({ session });

        // Commit transaction
        await session.commitTransaction();

        // Populate for response
        await order[0].populate('items.foodId', 'name image');

        res.status(201).json({
            success: true,
            message: 'Order created successfully',
            data: order[0]
        });

    } catch (error) {
        // Rollback transaction on error
        await session.abortTransaction();
        next(error);
    } finally {
        session.endSession();
    }
};

/**
 * @desc    Get user's orders
 * @route   GET /api/orders/my-orders
 * @access  Private
 */
const getMyOrders = async (req, res, next) => {
    try {
        const userId = req.user._id;
        const { page = 1, limit = 10, status } = req.query;

        const query = { user: userId };
        if (status) query.orderStatus = status;

        const skip = (parseInt(page) - 1) * parseInt(limit);

        const orders = await Order.find(query)
            .sort({ createdAt: -1 })
            .skip(skip)
            .limit(parseInt(limit))
            .select('orderNumber totalAmount orderStatus paymentMethod createdAt items');

        const totalOrders = await Order.countDocuments(query);

        res.json({
            success: true,
            count: orders.length,
            total: totalOrders,
            page: parseInt(page),
            totalPages: Math.ceil(totalOrders / parseInt(limit)),
            data: orders
        });

    } catch (error) {
        next(error);
    }
};

/**
 * @desc    Get single order by ID
 * @route   GET /api/orders/:id
 * @access  Private
 */
const getOrderById = async (req, res, next) => {
    try {
        const { id } = req.params;
        const userId = req.user._id;
        const isAdmin = req.user.role === 'admin';

        const order = await Order.findById(id)
            .populate('items.foodId', 'name image category')
            .populate('user', 'name email phone');

        if (!order) {
            return res.status(404).json({
                success: false,
                message: 'Order not found'
            });
        }

        // Check if user owns the order or is admin
        if (order.user._id.toString() !== userId.toString() && !isAdmin) {
            return res.status(403).json({
                success: false,
                message: 'Not authorized to view this order'
            });
        }

        // Add status descriptions
        const orderWithDesc = order.toObject();
        orderWithDesc.statusHistory = order.statusHistory.map(h => ({
            ...h.toObject(),
            description: getStatusDescription(h.status)
        }));

        res.json({
            success: true,
            data: orderWithDesc
        });

    } catch (error) {
        next(error);
    }
};

/**
 * @desc    Cancel order
 * @route   PUT /api/orders/:id/cancel
 * @access  Private
 */
const cancelOrder = async (req, res, next) => {
    try {
        const { id } = req.params;
        const { reason } = req.body;
        const userId = req.user._id;

        const order = await Order.findById(id);

        if (!order) {
            return res.status(404).json({
                success: false,
                message: 'Order not found'
            });
        }

        // Check if user owns the order
        if (order.user.toString() !== userId.toString()) {
            return res.status(403).json({
                success: false,
                message: 'Not authorized to cancel this order'
            });
        }

        // Check if order can be cancelled (only pending or confirmed)
        const cancellableStatuses = ['pending', 'confirmed'];
        if (!cancellableStatuses.includes(order.orderStatus)) {
            return res.status(400).json({
                success: false,
                message: `Order cannot be cancelled at ${order.orderStatus} stage`
            });
        }

        // Update order
        order.orderStatus = 'cancelled';
        order.cancelledAt = new Date();
        order.cancellationReason = reason || 'Cancelled by user';
        order.cancelledBy = userId;
        
        order.statusHistory.push({
            status: 'cancelled',
            note: reason || 'Order cancelled by user',
            updatedBy: userId
        });

        await order.save();

        res.json({
            success: true,
            message: 'Order cancelled successfully',
            data: {
                orderNumber: order.orderNumber,
                orderStatus: order.orderStatus,
                cancelledAt: order.cancelledAt
            }
        });

    } catch (error) {
        next(error);
    }
};
