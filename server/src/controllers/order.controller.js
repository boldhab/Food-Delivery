/**
 * @desc    Update order status (Admin)
 * @route   PUT /api/orders/admin/:id/status
 * @access  Private/Admin
 */
const updateOrderStatus = async (req, res, next) => {
    try {
        const { id } = req.params;
        const { status: rawStatus, note } = req.body || {};
        const status = normalizeOrderStatus(rawStatus);
        const allowedStatuses = [
            'pending',
            'confirmed',
            'preparing',
            'out_for_delivery',
            'delivered',
            'cancelled',
            'rejected'
        ];

        if (!status) {
            return res.status(400).json({
                success: false,
                message: 'status is required'
            });
        }
        if (!allowedStatuses.includes(status)) {
            return res.status(400).json({
                success: false,
                message: `Invalid status: ${status}`
            });
        }

        const order = await Order.findById(id);
        if (!order) {
            return res.status(404).json({
                success: false,
                message: 'Order not found'
            });
        }

        // No-op update should not fail
        if (order.orderStatus === status) {
            return res.json({
                success: true,
                message: 'Order status unchanged',
                data: {
                    orderId: order._id,
                    orderNumber: order.orderNumber,
                    orderStatus: order.orderStatus,
                    paymentStatus: order.paymentStatus
                }
            });
        }

        // Admin override: allow direct status update even across non-linear transitions.
        // Keep original transition utility available for non-admin workflows.
        order.orderStatus = status;

        if (status === 'delivered') {
            order.actualDeliveryTime = new Date();
            order.paymentStatus = 'paid';
        }

        if (status === 'cancelled' || status === 'rejected') {
            order.cancelledAt = new Date();
            order.cancellationReason = note || `Order ${status} by admin`;
            order.cancelledBy = req.user?._id;
        }

        order.statusHistory.push({
            status,
            note: note || `Status updated to ${status}`,
            updatedBy: req.user?._id
        });

        await order.save();

        res.json({
            success: true,
            message: 'Order status updated successfully',
            data: {
                orderId: order._id,
                orderNumber: order.orderNumber,
                orderStatus: order.orderStatus,
                paymentStatus: order.paymentStatus
            }
        });
    } catch (error) {
        next(error);
    }
};

/**
 * @desc    Get all orders with advanced filters (Admin)
 * @route   GET /api/orders/admin/all
 * @access  Private/Admin
 */
const getAllOrders = async (req, res, next) => {
    try {
        const {
            page = 1,
            limit = 10,
            status: rawStatus,
            paymentStatus,
            driver,
            startDate,
            endDate,
            search,
            sortBy = 'createdAt',
            sortOrder = 'desc'
        } = req.query;
        const statusFilter = buildOrderStatusFilter(rawStatus);

        // Build filter query
        const query = {};
        
        if (statusFilter) query.orderStatus = statusFilter;
        if (paymentStatus) query.paymentStatus = paymentStatus;
        if (driver) query.driver = driver;
        
        // Date range filter
        if (startDate || endDate) {
            query.createdAt = {};
            if (startDate) query.createdAt.$gte = new Date(startDate);
            if (endDate) {
                const end = new Date(endDate);
                end.setHours(23, 59, 59, 999);
                query.createdAt.$lte = end;
            }
        }

        // Search by order number or user name/email
        if (search) {
            query.$or = [
                { orderNumber: { $regex: search, $options: 'i' } },
                { 'userDetails.name': { $regex: search, $options: 'i' } },
                { 'userDetails.email': { $regex: search, $options: 'i' } }
            ];
        }

        // Pagination
        const skip = (parseInt(page) - 1) * parseInt(limit);
        
        // Sorting
        const sort = {};
        sort[sortBy] = sortOrder === 'desc' ? -1 : 1;

        // Execute query with population
        const orders = await Order.find(query)
            .populate('user', 'name email phone')
            .populate('driver', 'name email phone')
            .sort(sort)
            .skip(skip)
            .limit(parseInt(limit));

        // Get total count for pagination
        const totalOrders = await Order.countDocuments(query);

        // Get summary statistics for filtered results
        const summary = await Order.aggregate([
            { $match: query },
            {
                $group: {
                    _id: null,
                    totalRevenue: { $sum: '$totalAmount' },
                    averageOrderValue: { $avg: '$totalAmount' },
                    minOrderValue: { $min: '$totalAmount' },
                    maxOrderValue: { $max: '$totalAmount' },
                    orderCount: { $sum: 1 }
                }
            }
        ]);

        res.json({
            success: true,
            data: {
                orders,
                pagination: {
                    page: parseInt(page),
                    limit: parseInt(limit),
                    total: totalOrders,
                    pages: Math.ceil(totalOrders / parseInt(limit))
                },
                summary: summary[0] || {
                    totalRevenue: 0,
                    averageOrderValue: 0,
                    orderCount: 0
                }
            }
        });

    } catch (error) {
        next(error);
    }
};

