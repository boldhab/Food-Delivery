/**
 * @desc    Get dashboard statistics (Admin)
 * @route   GET /api/orders/admin/stats
 * @access  Private/Admin
 */
const getDashboardStats = async (req, res, next) => {
    try {
        const now = new Date();
        const startOfDay = new Date(now.setHours(0, 0, 0, 0));
        const startOfMonth = new Date(now.getFullYear(), now.getMonth(), 1);
        const startOfYear = new Date(now.getFullYear(), 0, 1);

        // Run multiple aggregations in parallel
        const [
            overallStats,
            todayStats,
            monthStats,
            statusStats,
            paymentStats,
            topSellingFoods,
            recentOrders
        ] = await Promise.all([
            // Overall statistics
            Order.aggregate([
                {
                    $group: {
                        _id: null,
                        totalRevenue: { $sum: '$totalAmount' },
                        totalOrders: { $sum: 1 },
                        averageOrderValue: { $avg: '$totalAmount' },
                        cancelledOrders: {
                            $sum: { $cond: [{ $eq: ['$orderStatus', 'cancelled'] }, 1, 0] }
                        },
                        completedOrders: {
                            $sum: { $cond: [{ $eq: ['$orderStatus', 'delivered'] }, 1, 0] }
                        }
                    }
                }
            ]),

            // Today's statistics
            Order.aggregate([
                { $match: { createdAt: { $gte: startOfDay } } },
                {
                    $group: {
                        _id: null,
                        revenue: { $sum: '$totalAmount' },
                        orders: { $sum: 1 }
                    }
                }
            ]),

            // This month's statistics
            Order.aggregate([
                { $match: { createdAt: { $gte: startOfMonth } } },
                {
                    $group: {
                        _id: null,
                        revenue: { $sum: '$totalAmount' },
                        orders: { $sum: 1 }
                    }
                }
            ]),

            // Orders by status
            Order.aggregate([
                {
                    $group: {
                        _id: '$orderStatus',
                        count: { $sum: 1 },
                        revenue: { $sum: '$totalAmount' }
                    }
                },
                { $sort: { count: -1 } }
            ]),

            // Orders by payment method
            Order.aggregate([
                {
                    $group: {
                        _id: '$paymentMethod',
                        count: { $sum: 1 },
                        total: { $sum: '$totalAmount' }
                    }
                }
            ]),

            // Top selling foods
            Order.aggregate([
                { $unwind: '$items' },
                {
                    $group: {
                        _id: '$items.foodId',
                        name: { $first: '$items.name' },
                        quantity: { $sum: '$items.quantity' },
                        revenue: { $sum: '$items.totalPrice' },
                        orderCount: { $sum: 1 }
                    }
                },
                { $sort: { quantity: -1 } },
                { $limit: 5 },
                {
                    $lookup: {
                        from: 'foods',
                        localField: '_id',
                        foreignField: '_id',
                        as: 'foodDetails'
                    }
                },
                { $unwind: { path: '$foodDetails', preserveNullAndEmptyArrays: true } }
            ]),

            // Recent orders for activity feed
            Order.find()
                .sort({ createdAt: -1 })
                .limit(5)
                .select('orderNumber totalAmount orderStatus userDetails createdAt')
        ]);

        res.json({
            success: true,
            data: {
                overview: {
                    totalRevenue: overallStats[0]?.totalRevenue || 0,
                    totalOrders: overallStats[0]?.totalOrders || 0,
                    averageOrderValue: overallStats[0]?.averageOrderValue || 0,
                    completedOrders: overallStats[0]?.completedOrders || 0,
                    cancelledOrders: overallStats[0]?.cancelledOrders || 0
                },
                today: {
                    revenue: todayStats[0]?.revenue || 0,
                    orders: todayStats[0]?.orders || 0
                },
                thisMonth: {
                    revenue: monthStats[0]?.revenue || 0,
                    orders: monthStats[0]?.orders || 0
                },
                ordersByStatus: statusStats,
                paymentMethods: paymentStats,
                topSellingFoods: topSellingFoods.map(food => ({
                    id: food._id,
                    name: food.name,
                    quantity: food.quantity,
                    revenue: food.revenue,
                    image: food.foodDetails?.image
                })),
                recentOrders
            }
        });

    } catch (error) {
        next(error);
    }
};

/**
 * @desc    Get sales report with charts data (Admin)
 * @route   GET /api/orders/admin/sales-report
 * @access  Private/Admin
 */
const getSalesReport = async (req, res, next) => {
    try {
        const { period = 'daily', days = 7 } = req.query;
        
        let groupFormat;
        let dateRange = new Date();
        dateRange.setDate(dateRange.getDate() - days);

        // Set grouping based on period
        switch(period) {
            case 'hourly':
                groupFormat = { $dateToString: { format: '%Y-%m-%d %H:00', date: '$createdAt' } };
                break;
            case 'daily':
                groupFormat = { $dateToString: { format: '%Y-%m-%d', date: '$createdAt' } };
                break;
            case 'weekly':
                groupFormat = { $week: '$createdAt' };
                break;
            case 'monthly':
                groupFormat = { $month: '$createdAt' };
                break;
            default:
                groupFormat = { $dateToString: { format: '%Y-%m-%d', date: '$createdAt' } };
        }

        const salesData = await Order.aggregate([
            {
                $match: {
                    createdAt: { $gte: dateRange },
                    orderStatus: { $ne: 'cancelled' }
                }
            },
            {
                $group: {
                    _id: groupFormat,
                    orders: { $sum: 1 },
                    revenue: { $sum: '$totalAmount' },
                    avgOrderValue: { $avg: '$totalAmount' }
                }
            },
            { $sort: { _id: 1 } }
        ]);

        // Get category-wise sales
        const categorySales = await Order.aggregate([
            { $match: { createdAt: { $gte: dateRange } } },
            { $unwind: '$items' },
            {
                $lookup: {
                    from: 'foods',
                    localField: 'items.foodId',
                    foreignField: '_id',
                    as: 'food'
                }
            },
            { $unwind: '$food' },
            {
                $group: {
                    _id: '$food.category',
                    quantity: { $sum: '$items.quantity' },
                    revenue: { $sum: '$items.totalPrice' }
                }
            },
            { $sort: { revenue: -1 } }
        ]);

        res.json({
            success: true,
            data: {
                period,
                salesData,
                categorySales
            }
        });

    } catch (error) {
        next(error);
    }
};

