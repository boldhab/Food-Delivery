const notifyAdmins = async ({ title, message, link, metadata = {} }) => {
    const admins = await User.find({ role: 'admin', isActive: true }).select('_id');
    if (!admins.length) return;

    await Notification.insertMany(
        admins.map((adminUser) => ({
            recipient: adminUser._id,
            type: 'info',
            title,
            message,
            link,
            metadata
        }))
    );
};

const canAccessOrderConversation = (order, user) => {
    if (!order || !user) return false;
    if (user.role === 'admin') return true;
    if (user.role === 'user') return order.user.toString() === user._id.toString();
    if (user.role === 'driver') {
        return order.driver && order.driver.toString() === user._id.toString();
    }
    return false;
};

const mapOrderCommunication = (entry) => ({
    _id: entry._id,
    sender: entry.sender,
    senderRole: entry.senderRole,
    message: entry.message,
    createdAt: entry.createdAt
});

const normalizeOrderStatus = (status) => {
    if (!status || typeof status !== 'string') return status;
    const normalized = status.trim().toLowerCase();
    if (normalized === 'complete' || normalized === 'completed') return 'delivered';
    if (normalized === 'canceled') return 'cancelled';
    return normalized;
};

const buildOrderStatusFilter = (status) => {
    const normalized = normalizeOrderStatus(status);
    if (!normalized) return null;

    if (normalized === 'delivered') {
        return { $in: ['delivered', 'completed', 'complete'] };
    }

    if (normalized === 'cancelled') {
        return { $in: ['cancelled', 'canceled'] };
    }

    return normalized;
};

